package com.faqkeyword.model;


public class FaqKeyWordVO {
	private Integer kwID;
	private String kwContent;
	private String kwReply;
	private Integer kwStatus;

	public Integer getKwID() {
		return kwID;
	}

	public void setKwID(Integer kwID) {
		this.kwID = kwID;
	}

	public String getKwContent() {
		return kwContent;
	}

	public void setKwContent(String kwContent) {
		this.kwContent = kwContent;
	}

	public String getKwReply() {
		return kwReply;
	}

	public void setKwReply(String kwReply) {
		this.kwReply = kwReply;
	}

	public Integer getKwStatus() {
		return kwStatus;
	}

	public void setKwStatus(Integer kwStatus) {
		this.kwStatus = kwStatus;
	}

}
